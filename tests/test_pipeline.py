# ── ML model tests ────────────────────────────────────────────────────────────

class TestMLModel:
    def test_feature_matrix_shape(self, processed):
        X, y, names = build_feature_matrix(processed)
        assert X.shape[0] == y.shape[0]
        assert X.shape[1] == len(names)
        assert len(names) >= 4

    def test_target_binary(self, processed):
        _, y, _ = build_feature_matrix(processed)
        assert set(np.unique(y)).issubset({0, 1})

    def test_cv_auc_in_range(self, processed):
        results = train_and_evaluate(processed, n_estimators=50, cv_folds=3)
        auc = results["cv_auc_mean"]
        assert 0.45 <= auc <= 0.65, f"CV AUC {auc} out of expected range."

    def test_importances_sum_to_one(self, processed):
        results = train_and_evaluate(processed, n_estimators=50, cv_folds=3)
        total = sum(results["importances"].values())
        assert abs(total - 1.0) < 0.01

    def test_fg_in_top4_features(self, processed):
        """FG index should rank in top 4 (consistent with report's 3rd place finding)."""
        results = train_and_evaluate(processed, n_estimators=50, cv_folds=3)
        ranked  = sorted(results["importances"], key=results["importances"].get, reverse=True)
        fg_col  = next(c for c in ranked if "fg" in c.lower() or "value" in c.lower())
        assert ranked.index(fg_col) < 4, f"FG index ranked {ranked.index(fg_col)+1}, expected top 4."


# ── Insights tests ────────────────────────────────────────────────────────────

class TestInsights:
    def test_recommendations_count(self):
        assert len(RECOMMENDATIONS) == 6

    def test_recommendations_fields(self):
        for rec in RECOMMENDATIONS:
            for field in ["id", "title", "action", "priority"]:
                assert field in rec, f"Missing field '{field}' in recommendation {rec.get('id')}"

    def test_key_stats(self, processed):
        trader_df = build_trader_summary(processed)
        stats = compute_key_stats(processed, trader_df)
        assert 0 < stats["pct_profitable_traders"] < 100
        assert stats["low_leverage_win_rate"] >= stats["high_leverage_win_rate"] - 20


