# ── Data loading tests ────────────────────────────────────────────────────────

class TestDataLoading:
    def test_trades_shape(self, raw_data):
        trades, _ = raw_data
        assert len(trades) == 20_000, "Expected 20,000 trade rows."

    def test_fg_length(self, raw_data):
        _, fg = raw_data
        assert len(fg) >= 480, "Expected ~500 Fear & Greed rows."

    def test_trades_columns(self, raw_data):
        trades, _ = raw_data
        required = {"account", "symbol", "closedPnL", "leverage", "size"}
        assert required.issubset(set(trades.columns))

    def test_no_duplicate_timestamps(self, raw_data):
        trades, _ = raw_data
        # Trades can share a timestamp but the (timestamp, account, symbol) triple should not
        dups = trades.duplicated(subset=["timestamp", "account", "symbol", "side"])
        assert dups.sum() / len(trades) < 0.01, "Too many duplicate entries."


